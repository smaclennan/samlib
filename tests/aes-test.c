static int test_cbc256(void)
{
	int rc1 = 0, rc2 = 0;
	uint8_t digest[AES_BLOCK_SIZE];
	aes128_ctx ctx;

	for (int i = 0; i < 4; ++i) {
		AES_CBC_init_ctx(&ctx, aes256_vectors[i].key, aes256_vectors[i].iv, 256, 1);

		AES_CBC_encrypt(&ctx, aes256_vectors[i].in, AES_BLOCK_SIZE, digest);

		rc1 |= memcmp(aes256_vectors[i].out, digest, sizeof(digest));
	}

	out_msg("CBC256 encrypt", rc1, ctx.have_hw);

	for (int i = 0; i < 4; ++i) {
		AES_CBC_init_ctx(&ctx, aes256_vectors[i].key, aes256_vectors[i].iv, 256, 0);

		AES_CBC_decrypt(&ctx, aes256_vectors[i].out, AES_BLOCK_SIZE, digest);

		rc2 |= memcmp(aes256_vectors[i].in, digest, sizeof(digest));
	}

	out_msg("CBC256 decrypt", rc2, ctx.have_hw);

	return rc1 | rc2;
}

