static int do_testsuite(void)
{
	int rc = 0;

	rc |= test_decrypt_ecb();
	rc |= test_encrypt_ecb();
	rc |= test_encrypt_ecb_malloc();
	rc |= test_encrypt_decrypt_cbc();
	rc |= test_cbc128();
	rc |= test_cbc256();

	return rc;
}

